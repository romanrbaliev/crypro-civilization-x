const loadFromTelegramCloudStorage = async (): Promise<GameState | null> => {
  if (!isTelegramWebAppAvailable()) {
    return null;
  }
  
  try {
    console.log('üîÑ –ü–æ–ø—ã—Ç–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –∏–∑ Telegram CloudStorage...');
    
    // –ò—Å–ø–æ–ª—å–∑—É–µ–º –≤—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é –¥–ª—è –∑–∞–≥—Ä—É–∑–∫–∏
    const jsonData = await forceTelegramCloudLoad(TG_CLOUD_KEY);
    
    if (jsonData) {
      try {
        const parsedData = JSON.parse(jsonData);
        if (parsedData && parsedData.gameData) {
          console.log('‚úÖ –ò–≥—Ä–∞ —É—Å–ø–µ—à–Ω–æ –∑–∞–≥—Ä—É–∂–µ–Ω–∞ –∏–∑ Telegram CloudStorage');
          return parsedData.gameData as GameState;
        }
      } catch (parseError) {
        console.error('‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ —Ä–∞–∑–±–æ—Ä–µ –¥–∞–Ω–Ω—ã—Ö –∏–∑ Telegram CloudStorage:', parseError);
      }
    }
    
    console.warn('‚ö†Ô∏è –î–∞–Ω–Ω—ã–µ –≤ Telegram CloudStorage –Ω–µ –Ω–∞–π–¥–µ–Ω—ã –∏–ª–∏ –ø–æ–≤—Ä–µ–∂–¥–µ–Ω—ã');
    return null;
  } catch (error) {
    console.error('‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ –∏–∑ Telegram CloudStorage:', error);
    return null;
  }
};

